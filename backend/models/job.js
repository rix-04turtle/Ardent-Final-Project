const mongoose = require('mongoose');

const jobSchema = new mongoose.Schema({
  title: {
    type: String,required: true
  },
  description: {
    type: String, required: true
  },
  company: {    
    type: String, required: true
  },
  location: {   
    type: String, required: true
  },
  salary: {
    type: String, required: true
  },
  jobType: {
    type: String, required: true
  },
  jobimage: {
    type: String, required: true
  }
},

{timestamps: true});

module.exports = mongoose.model('Job-fiem-ardent', jobSchema);
